#include <stdio.h>
#include <math.h>

#define N 21

void cholesky(double A[N][N], double L[N][N]) {
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < (i + 1); j++) {
            double aux = 0;
            for (int k = 0; k < j; k++) {
                aux += L[i][k] * L[j][k];
            }
            if (i == j) {
                L[i][j] = sqrt(A[i][i] - aux);
            } else {
                L[i][j] = (A[i][j] - aux) / L[j][j];
            }
        }
    }
}

void printMatriz(double matriz[N][N]) {
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            printf("%.2f ", matriz[i][j]);
        }
        printf("\n");
    }
}

void determinar(double A[N][N]){
    double det;
    det = A[0][0] * A[1][1];
    if(det <= 0){
        printf(" N eh simetrica definida positiva \n \n");
    }
    else{
        for(int i = 0; i < N; i++){
            det = A[i][i] * A[i][i];
            if(det <= 0){
            printf(" N eh simtrica definida positiva\n \n");
            break;
        }
    }
}
}

